
# add
def add(a,b):
	return int(a+b)

# subtract
def subtract(a,b):
	return int(a-b)

# multiply
def multiply(a,b):
	return int(a*b)

# divide
def divide(a,b):
	return int(a/b)

# square
def square(a):
	return int(a*a)

# square root
def square_root(a):
	return int(a**0.5)	

